#Realizar un programa que, dada una cantidad de dólares y su cotización permita obtener el equivalente en pesos.  
cantidad_dolares = float(input("Ingrese la cotizacion del Dolar En Pesos Argentinos:\n"))
cotizacion_dolar = float(input("Ingrese la cantidad de Dolares a convertir:\n"))

print("Resultado: ",cantidad_dolares * cotizacion_dolar,"ARS")


