#Realizar un programa que permita obtener la suma, resta, multiplicación y división de dos números x e y ingresados por teclado. El programa debe imprimir los números pedidos y el resultado de las cuatro operaciones.   
numero_x= float(input("Ingrese valor de  X:\n"))
numero_y = float(input("Ingrese valor de  X:\n"))

print("Resultado Suma:", numero_x + numero_y)
print("Resultado Resta:", numero_x - numero_y)
print("Resultado Multiplicacion:", numero_x * numero_y)
print("Resultado Division:", numero_x / numero_y)



